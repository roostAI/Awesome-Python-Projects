import unittest
from sudoku import solve

class TestSudokuSolver(unittest.TestCase):

    def test_Solve_e4f6c3e14b(self):
        # Test case 1: Testing with a valid Sudoku board
        board1 = [
            [5,3,0,0,7,0,0,0,0],
            [6,0,0,1,9,5,0,0,0],
            [0,9,8,0,0,0,0,6,0],
            [8,0,0,0,6,0,0,0,3],
            [4,0,0,8,0,3,0,0,1],
            [7,0,0,0,2,0,0,0,6],
            [0,6,0,0,0,0,2,8,0],
            [0,0,0,4,1,9,0,0,5],
            [0,0,0,0,8,0,0,7,9]
        ]
        self.assertEqual(solve(board1), True)

        # Test case 2: Testing with an invalid Sudoku board
        board2 = [
            [5,5,0,0,7,0,0,0,0],
            [6,0,0,1,9,5,0,0,0],
            [0,9,8,0,0,0,0,6,0],
            [8,0,0,0,6,0,0,0,3],
            [4,0,0,8,0,3,0,0,1],
            [7,0,0,0,2,0,0,0,6],
            [0,6,0,0,0,0,2,8,0],
            [0,0,0,4,1,9,0,0,5],
            [0,0,0,0,8,0,0,7,9]
        ]
        self.assertEqual(solve(board2), False)

if __name__ == '__main__':
    unittest.main()
